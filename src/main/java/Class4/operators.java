public class operators {

    public static void main(String[] args) {

        int x=20;
        int y=10;

        int sum= x+y;
        System.out.println("sum:"+sum);
        int sub=x-y;
        System.out.println("sub:"+sub);
        int div=x/y;
        System.out.println("div:"+div);
        int mul=x*y;
        System.out.println("mul:"+mul);
        int rem=x%y;
        System.out.println("rem:"+rem);
    }
}
